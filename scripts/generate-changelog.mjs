#!/usr/bin/env bun
// scripts/generate-changelog.mjs
//
// Generates lib/generated/system-prompt.ts with the static chat system
// prompt (PRIMORDIA.md + last 30 changelog filenames) baked in at build time.
//
// NOTE: public/changelog.json is no longer generated here. The /changelog page
// now reads the changelog/ directory directly at runtime and lazy-loads each
// entry's body via /api/changelog?filename=... when the user expands it.
//
// Filename convention: YYYY-MM-DD-HH-MM-SS Description of change.md
//   - The date+time portion is used for sorting (newest first).
//   - Everything after the first space is the human-readable title.
//
// Run automatically as a prebuild/predev step (see package.json).

import { readFileSync, readdirSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const changelogDir = join(repoRoot, "changelog");

// Matches: YYYY-MM-DD-HH-MM-SS Description of change.md
const FILENAME_RE = /^(\d{4}-\d{2}-\d{2})-(\d{2}-\d{2}-\d{2}) (.+)\.md$/;

let filenames = [];

try {
  filenames = readdirSync(changelogDir)
    .filter((f) => FILENAME_RE.test(f))
    .sort()    // lexicographic sort = chronological order
    .reverse(); // newest first
} catch (e) {
  console.warn("changelog: could not read changelog/ directory:", e.message);
}

// --- Generate static system prompt for the chat API ---

// Read PRIMORDIA.md to give the assistant self-knowledge about architecture.
let primordiaContent = "";
try {
  primordiaContent = readFileSync(join(repoRoot, "PRIMORDIA.md"), "utf-8").trim();
} catch (e) {
  console.warn("changelog: could not read PRIMORDIA.md:", e.message);
}

// Take the last 30 changelog filenames (filenames is already newest-first).
const last30 = filenames.slice(0, 30);
const changelogSection =
  last30.length > 0
    ? "## Changelog Entries (filename = short description)\n" +
      last30.map((f) => `- ${f.replace(/\.md$/, "")}`).join("\n")
    : "";

const primordiaContext = [primordiaContent, changelogSection]
  .filter(Boolean)
  .join("\n\n---\n\n");

const systemPrompt = `You are the AI assistant embedded in Primordia, a self-modifying web application.
You help users accomplish tasks and answer questions. Be concise and helpful.
When users seem interested in changing the app itself, remind them they can switch to "evolve mode" to propose changes.

You have access to two tools that let you read the live project files:
- list_directory(path): list files/subdirectories inside a project directory (dotfiles excluded)
- read_file(path): read the contents of a file (dotfiles blocked for security)
Use these tools when a user asks about specific files, current code, or project structure beyond what PRIMORDIA.md covers.

Below is the full architecture document and changelog for Primordia. Use this as the source of truth when answering questions about how the app works, what technologies it uses, or what has changed.

${primordiaContext}`;

// Write as a TypeScript module so route.ts can import it as a static constant.
const libGeneratedDir = join(repoRoot, "lib", "generated");
mkdirSync(libGeneratedDir, { recursive: true });
writeFileSync(
  join(libGeneratedDir, "system-prompt.ts"),
  `// AUTO-GENERATED by scripts/generate-changelog.mjs — do not edit by hand\n// Regenerate via: bun run predev\n\nexport const SYSTEM_PROMPT = ${JSON.stringify(systemPrompt)};\n`
);
console.log(`changelog: wrote static system prompt → lib/generated/system-prompt.ts (${filenames.length} entries)`);
